'''EX 10 - is it even? '''

oddeven= int(input("Please enter a random number: ")) #user input number in integer

if oddeven % 2 == 0: #if the number remainder divided by 2 is 0
    print ("number given is even") #then the number is even
else:#else when the given number has a remainder
    print ("number given is odd") #then the number is odd 
    
